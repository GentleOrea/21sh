#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		write(1, "-", 1);
		if (n < -9)
			ft_putnbr(-(n / 10));
		ft_putchar((char)(n < 0 ? '0' - (n % 10) : '0' + (n % 10)));
		return ;
	}
	else if (n < 10)
		ft_putchar((char)('0' + (n % 10)));
	else
	{
		ft_putnbr(n / 10);
		ft_putchar((char)('0' + (n % 10)));
	}
}
