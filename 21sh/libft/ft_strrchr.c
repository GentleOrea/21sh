#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0 && s[i] != (char)c)
		i--;
	return (i >= 0 ? (char *)&(s[i]) : NULL);
}
