#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	m;

	m = 0;
	while (s1 && s2 && s1[m] && s1[m] == s2[m] && m < n)
		m++;
	if (s1 && s2)
		return (s1[m] == s2[m] || m == n ? 1 : 0);
	return (0);
}
