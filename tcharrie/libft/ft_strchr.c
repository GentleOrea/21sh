#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i] != (char)c && s[i])
		i++;
	return (s[i] == (char)c ? (char *)&(s[i]) : NULL);
}
