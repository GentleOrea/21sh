#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	n;

	if (!s2)
		return (s1);
	if (!s1)
		return ((char*)s2);
	n = 0;
	while (s1[n])
		n++;
	while (*s2)
	{
		s1[n] = *s2;
		n++;
		s2++;
	}
	s1[n] = '\0';
	return (s1);
}
