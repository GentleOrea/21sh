#include "libft.h"

int	ft_gotmod(size_t nb, int i)
{
	if (nb == 0)
		return (i == 0);
	while (nb > 0)
	{
		if (nb % 10 == (size_t)i)
			return (1);
		nb /= 10;
	}
	return (0);
}
