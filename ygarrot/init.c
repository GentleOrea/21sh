void		f_point(t_shell *sh)
{
	sh->f_built[0] = &ft_echo;
	sh->f_built[1] = &ft_cd;
	sh->f_built[2] = &ft_env;
	sh->f_built[3] = &ft_setenv;
	sh->f_built[4] = &ft_unsetenv;
}
